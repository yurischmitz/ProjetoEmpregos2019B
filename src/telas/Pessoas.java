    private void jtbPessoaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbPessoaMousePressed
        try{

            int linhaSelecionada = jtbPessoa.getSelectedRow();//pega a linha selecionada
            String codigo = jtbPessoa.getModel().getValueAt(linhaSelecionada, 0).toString(); // Primeira coluna da linha

            //Verifica se clicou na coluna 4 = EXCLUIR
            if(jtbPessoa.isColumnSelected(4)){
                try{
                    boolean wPergunta = CaixaDeDialogo.obterinstancia().pedirConfirmacao("Tem certeza de que deseja excluir?","",'p');
                    if (wPergunta == true){
                        objPessoa = new Pessoa();
                        objPessoa.setId(Integer.parseInt(codigo));

                        objPessoaControle = new PessoaControle(objPessoa, null);
                        boolean wControle = objPessoaControle.excluir();

                        if (wControle){
                            CaixaDeDialogo.obterinstancia().exibirMensagem("ExcluÃ­do com Sucesso!");
                        }else{
                            CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao excluir!");
                        }
                    }
                    atualizarTabela();

                }catch(Exception ex){
                    CaixaDeDialogo.obterinstancia().exibirMensagem("Erro: " + ex.getMessage());
                }
            }else{

                objPessoaControle = new PessoaControle(null, null);
                objPessoa = objPessoaControle.buscar(codigo);
                if (objPessoa != null && objPessoa.getId() > 0){
                    preencherCampos();
                }else{
                    CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao buscar no BD!");
                }
            }

        }catch(Exception ex){
            CaixaDeDialogo.obterinstancia().exibirMensagem(ex.getMessage(), 'e');
        }
    }//GEN-LAST:event_jtbPessoaMousePressed

