    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try{
            boolean retorno;
            //validar os campos
            if(txtNome.getText().trim().length() == 0){
                CaixaDeDialogo.obterinstancia().exibirMensagem("Informe uma cidade corretamente", 'a');
                return;
            }
            
            if(!Validacao.validarCNPJ(txtCnpj.getText())){
                System.out.println(txtCnpj.getText());
                CaixaDeDialogo.obterinstancia().exibirMensagem("Cnpj errado!");
                return;
            }
            

            objEmpresa = new Empresa();
            objEmpresa.setNome(txtNome.getText().trim());
            objEmpresa.setCnpj(txtCnpj.getText().trim());

            if(!lblId.getText().equals("ID")){
                objEmpresa.setId(Integer.parseInt(lblId.getText()));
                objEmpresaControle = new EmpresaControle(objEmpresa, null);
                retorno = objEmpresaControle.alterar();
            }else{
                objEmpresaControle = new EmpresaControle(objEmpresa, null);
                retorno = objEmpresaControle.incluir();
            }

            if(retorno = true){
                CaixaDeDialogo.obterinstancia().exibirMensagem("Registro salvo");
            }else{
                CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao tentar salvar");
            }

            atualizarTabela();

        }catch(Exception ex){
            CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao tentar incluir");
            System.out.println("ERRO: " + ex.getMessage().toString());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void jtbEmpresasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbEmpresasMousePressed
        try{

            int linhaSelecionada = jtbEmpresas.getSelectedRow();//pega a linha selecionada
            String codigo = jtbEmpresas.getModel().getValueAt(linhaSelecionada, 0).toString(); // Primeira coluna da linha

            //Verifica se clicou na coluna 3 = EXCLUIR
            if(jtbEmpresas.isColumnSelected(3)){
                try{
                    boolean wPergunta = CaixaDeDialogo.obterinstancia().pedirConfirmacao("Tem certeza de que deseja excluir?","",'p');
                    if (wPergunta == true){
                        objEmpresa = new Empresa();
                        objEmpresa.setId(Integer.parseInt(codigo));

                        objEmpresaControle = new EmpresaControle(objEmpresa, null);
                        boolean wControle = objEmpresaControle.excluir();
                        if (wControle){
                            CaixaDeDialogo.obterinstancia().exibirMensagem("ExcluÃ­do com Sucesso!");
                        }else{
                            CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao excluir!");
                        }
                    }
                    atualizarTabela();

                }catch(Exception ex){
                    CaixaDeDialogo.obterinstancia().exibirMensagem("Erro: " + ex.getMessage());
                }
            }else{

                objEmpresaControle = new EmpresaControle(null, null);
                objEmpresa = objEmpresaControle.buscar(codigo);
                if (objEmpresa != null && objEmpresa.getId() > 0){
                    preencherCampos();
                }else{
                    CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao buscar no BD!");
                }
            }

        }catch(Exception ex){
            CaixaDeDialogo.obterinstancia().exibirMensagem(ex.getMessage(), 'e');
        }
    }//GEN-LAST:event_jtbEmpresasMousePressed

